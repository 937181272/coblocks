/**
 * Set the attributes for the Text Panel transformations
 * @type {Object}
 */
function ButtonTransforms( props ) {

	const transforms = {
		button: props.button,
		buttonUrl: props.buttonUrl,
		buttonTitle: props.buttonTitle,
		buttonFontFamily: props.buttonFontFamily,
		buttonFontSize: props.buttonFontSize,
		customButtonFontSize: props.customButtonFontSize,
		buttonLineHeight: props.buttonLineHeight,
		button_2: props.button_2,
		buttonUrl_2: props.buttonUrl_2,
		buttonTitle_2: props.buttonTitle_2,
		buttonColor: props.buttonColor,
		customButtonColor: props.customButtonColor,
		spacerButton: props.spacerButton,
		buttonBackground: props.buttonBackground,
		customButtonBackground: props.customButtonBackground,
	};

	return transforms;
}

export default ButtonTransforms;